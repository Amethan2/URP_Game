/// <summary>
/// Перечисление для функциональных типов зданий в игре.
/// </summary>
public enum BuildingType
{
    /// <summary>
    /// Дом - производит население (крестьяне)
    /// </summary>
    House,

    /// <summary>
    /// Алтарь - генерирует ману и создает юнитов поддержки
    /// </summary>
    Altar,

    /// <summary>
    /// Башня - атакует врагов и создает защитные юниты
    /// </summary>
    Tower,

    /// <summary>
    /// Конюшня - производит быстрых юнитов (всадники)
    /// </summary>
    Stable,

    /// <summary>
    /// Казарма - производит пехоту и воинов
    /// </summary>
    Barracks,

    /// <summary>
    /// Мастерская - производит осадные машины и инженеров
    /// </summary>
    Workshop
} 